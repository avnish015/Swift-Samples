//Strong reference cycle in Class
class Customer {
    let name: String
    var card: CreditCard?
    init(name: String) {
        self.name = name
    }
    deinit { print("\(name) is being deinitialized") }
}

class CreditCard {
    let number: UInt64
    unowned var customer: Customer
    init(number: UInt64, customer: Customer) {
        self.number = number
        self.customer = customer
    }
    deinit { print("Card #\(number) is being deinitialized") }
}

var customer:Customer? = Customer(name: "Avnish")
var card:CreditCard? = CreditCard(number: 12345, customer: customer!)
customer!.card = card

customer = nil
print(card?.customer)

//Strong reference in Closure

class HTMLElement {
    
    let name: String
    let text: String?
    
    lazy var asHTML: () -> String = {
        if let text = self.text {
            return "<\(self.name)>\(text)</\(self.name)>"
        } else {
            return "<\(self.name) />"
        }
    }
    
    init(name: String, text: String? = nil) {
        self.name = name
        self.text = text
    }
    
    deinit {
        print("\(name) is being deinitialized")
    }
    
}

var obj:HTMLElement? = HTMLElement(name: "Avnish", text: "Hello")
obj?.asHTML()
obj = nil

